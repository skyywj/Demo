package basic.knowledge.sortAndSearch.深度优先遍历;

/**
 * @author CarryJey
 * @since 2018/10/30
 */
public class Node {
    private Integer node;
    private Node left;
    private Node right;

    public Integer getNode() {
        return node;
    }

    public void setNode(Integer node) {
        this.node = node;
    }

    public Node getLeft() {
        return left;
    }

    public void setLeft(Node left) {
        this.left = left;
    }

    public Node getRight() {
        return right;
    }

    public void setRight(Node right) {
        this.right = right;
    }
}
